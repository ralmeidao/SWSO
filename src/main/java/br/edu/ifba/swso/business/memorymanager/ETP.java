package br.edu.ifba.swso.business.memorymanager;

public class ETP {
	private int ppv;
	private int ppr;
	private char bitV;
	private char bitM;

	public int getPpv() {
		return ppv;
	}
	public void setPpv(int ppv) {
		this.ppv = ppv;
	}
	public int getPpr() {
		return ppr;
	}
	public void setPpr(int ppr) {
		this.ppr = ppr;
	}
	public char getBitV() {
		return bitV;
	}
	public void setBitV(char bitV) {
		this.bitV = bitV;
	}
	public char getBitM() {
		return bitM;
	}
	public void setBitM(char bitM) {
		this.bitM = bitM;
	}
}

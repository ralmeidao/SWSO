package br.edu.ifba.swso.display;

public class MovimentoCabecoteHD {

	private int posicaoDe;
	private int posicaoPara;
	
	public MovimentoCabecoteHD(int posicaoDe, int posicaoPara) {
		super();
		this.posicaoDe = posicaoDe;
		this.posicaoPara = posicaoPara;
	}
	
	public int getPosicaoDe() {
		return posicaoDe;
	}
	
	public int getPosicaoPara() {
		return posicaoPara;
	}
	
}
